#include "../includes/libft.h"

void			ft_putnbr(int n)
{
	long int	nb;

	nb = n;
	if (n < 0)
	{
		nb = -nb;
		ft_putchar('-');
	}
	if (nb / 10)
		ft_putnbr(nb / 10);
	ft_putchar((nb % 10) + 48);
}

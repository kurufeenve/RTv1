long long		ft_qpow(int n, int p)
{
	int		res;

	res = 1;
	while (p)
	{
		if (p & 1)
		{
			res *= n;
			p--;
		}
		else
		{
			n *= n;
			p >>= 1;
		}
	}
	return (res);
}

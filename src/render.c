#include "../includes/rtv1.h"

void	render(t_scene *sc)
{
	sc->i = -sc->Cw/2;
	while (sc->i < sc->Cw/2)
	{
		sc->j = -sc->Ch/2;
		while (sc->j < sc->Ch/2)
		{
			//printf("sc->i = %d, sc->i = %d\n", sc->i, sc->j);
			sc->j++;
		}
		sc->i++;
	}
}
